function Z = H_ZCdf(x)
    if x < 0
        neg = 1;
    else
        neg = 0;
    end
    
    if neg == 1
    x = x*-1;
    end
    
    k = (1 / ( 1 + 0.2316419 * x));
    y = (((( 1.330274429 * k - 1.821255978) * k + 1.781477937) * ...
        k - 0.356563782) * k + 0.319381530) * k;
    y = 1.0 - 0.3989422803   * exp(-0.5 * x * x) * y;

    Z = (1 - neg) * y + neg * (1.0 - y);
    
end
